// Efetuar a leitura de um valor numérico inteiro e classifica-lo como positivo ou negativo.

void main() {

  int numero = 1;

  if (numero > 0) {

    print("O número é positivo.");

  } 
  else if (numero < 0) {

    print("O número é negativo.");

  } 
  else {

    print("O número é zero.");

  }

}
